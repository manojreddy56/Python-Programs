def multiply(x: float, y: float) -> float:
    """
    Multiply two numbers.
    And return the result of multiplication.
    
    Args:
        x : the first number that was provided for multiplication
        y : the second number that was provided for multiplication

    Returns:
        returns the `result` of multiplication of those two numbers that were provided as arguments
    """
    result = x * y
    return result


print(multiply(10, 5))